#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;

	i = 0;
	if (n > 0)
	{
		while (n > i)
		{
			*(unsigned char *)((char *)s + i) = 0;
			i++;
		}
	}
}

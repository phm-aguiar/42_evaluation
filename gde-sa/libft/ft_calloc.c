#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	total_size;
	char	*ptr;

	total_size = nmemb * size;
	if (nmemb <= 0 || size <= 0)
		return (malloc(0));
	if (total_size / size != nmemb)
		return (NULL);
	ptr = malloc(nmemb * size);
	if (ptr == NULL)
		return (NULL);
	ft_bzero(ptr, nmemb * size);
	return ((int *)ptr);
}

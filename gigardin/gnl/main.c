#include "get_next_line.h"
#include <fcntl.h>
#include <stdio.h>

int	main(void)
{
	int		fd;
	char	*line;

	fd = open("test.txt", O_RDONLY);
	line = get_next_line(fd);
	printf("%s,%d", line,fd);

	close(fd);
	line = get_next_line(fd);
	printf("%s,%d", line,fd);
	usleep(50000);
	free(line);
return (0);
}
